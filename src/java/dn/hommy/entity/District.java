
package dn.hommy.entity;


public class District {
    
    private int district_iddistrict;
    private String wards_name;

    public District() {
    }

    public int getDistrict_iddistrict() {
        return district_iddistrict;
    }

    public void setDistrict_iddistrict(int district_iddistrict) {
        this.district_iddistrict = district_iddistrict;
    }

    public String getWards_name() {
        return wards_name;
    }

    public void setWards_name(String wards_name) {
        this.wards_name = wards_name;
    }
    
}
